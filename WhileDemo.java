package MyPack;
// in while loop first condition will check
// if the condition is true then only it will enter into loop
public class WhileDemo {

	public static void main(String[] args) 
	{
		int i=1;
		while(i<=10)
		{
			System.out.print(i);
			i++; // post incrementation, first vale will be print and then it will increment
		}
		

	}

}
